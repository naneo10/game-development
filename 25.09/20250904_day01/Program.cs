namespace _20250904
{//코드블럭이란? '{}'
    internal class Program
    {
        static void Main(string[] args)  //함수라 불리는 애들 중 특별하다 'Main' 진입점
        {// 시작점
            //우리가 당분간 사용할 지역
            //WriteLine : 출력을 담당하는 메서드
            Console.WriteLine("Hello, World!");
            Console.Write("난 개행이 없음(줄안바꿈)");
            Console.WriteLine("환영한다");
