            /* 
             
            ===================================
            [Main 메서드]
                -프로그램의 진입점(Entry Point)으로써 프로그램을 시작하면 실행
                -메인이 종료되면 프로그램도 종료
                -반드시 Main메서드는 무조건 있어야 한다

            [입출력]
                Console.WriteLine: 콘솔에 출력하고 줄을 변경한다
                Console.Write: 콘솔에 출력하고 줄을 바꾸지 않는다
                Console.ReadLine: 콘솔을 통해 한 줄 입력받음
                Console.Readkey: 콘솔을 통해 한 키를 입력받음
            주석문: 실행에 영향을 주지 않는 코드 설명문 / 프로그램 이름, 사용자 이름, 작성일 등을 필요한 내용을 기록하는 용도로 사용 / 프로그램을 컴파일 할 때 실행되지 않고, 실행파일에도 영향을 주지 않음
            여러줄 주석 : 원하는 만큼 영역을 드래그해서 ctrl+k+c || ctrl+/
            여러줄 주석해제 : 원하는 만큼 영역을 드래그해서 ctrl+k+u || Ctrl+/

            [이스케이프 시퀀스]
                -문자열 내에서 특별한 의미를 가지는 문자들을 표현하기 위해 사용
                -백슬래시(\)로 시작하며, 다음에 오는 문자가 특정한 기능을 수행
                -예시)
                \n : 줄바꿈
                \t : 탭
                \\ : 백슬래시 문자 자체
                \" : 큰 따옴표 문자 자체
                \' : 작은 따옴표 문자 자체
            ===================================

            */

            Console.Write("hello, C#\n");
            Console.Write("안녕, \tC# \n");

            //Day01 과제
            //아까 우리 자기소개 했던 내용을
            //Console.WriteLine와 Console.Write를 활용해서 출력하세요.

