                "정확한 타이밍에 맞춰 패턴을 파훼하는 것이 공통적이라 생각합니다. 그리고 이 공통점 때문에 두 장르를 좋아하고 즐겨합니다. \n리듬게임은 8살 때 부터 오락실에서 플레이를 해왔었고 소울라이크는 다크소울3로 처음 접해보고 반쯤 오기로 플레이하다가" +
                "즐기는 단계까지 가게 된 것 같습니다. 리듬게임과 소울라이크 두 장르 다 개개인의 실력향상이 주 목적이고 콘탠츠이기에 계속해서 플레이하고 있습니다.");

        }// 끝나는점
    }
